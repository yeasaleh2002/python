# python Arithmetic operators

'''
- Arithmetic operators are used with numeric values to perform common mathematical operations:

Operator	  Name	           Example	
    +	    Addition    	    x + y	
    -	    Subtraction 	    x - y	
    *	    Multiplication	    x * y	
    /	    Division	        x / y	
    %	    Modulus     	    x % y	
    **	    Exponentiation	    x ** y	# working as a power x^y
    //	    Floor division	    x // y  # removed from the floating point data. like (23.3 to 23)
'''

a = 10
b = 4;

# Addition
addition = a+b;
print(addition);

# Subtraction
subtraction = a-b;
print(subtraction);

# Multiplication
multiplication = a*b;
print(multiplication);

# Division
division = a/b;
print(division);

# modulus
modulus = a%b;
print(modulus);

# Exponentiation
exponentiation = a**b;
print(exponentiation);

# Floor division
floorDivision = a//b;
print(floorDivision);