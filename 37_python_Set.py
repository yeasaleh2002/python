# Python set datatype
'''
Set: sets are used to store multiple items in a single variable
- Sets are written with curly brackets - {}
- We can duplicate any value at the same times
- Duplicate value are not allow
- Set is one of 3 build in data types in python used to store collections of data, the other 3 are List, Tuple, and Dictionary, all with different qualiteies and usages.
- A set is a collection which is unordered, unchangeable*, and unidexed.
Note: Set items are unchangeable, but you can remove items and add new items.
'''

setData = {"Ab", "Ab", "Ac", "Ad", 1, 2, 3, 4, True, False};  # it will not follow the order of list
print("SetData", setData);
print("type", type(setData))
print("Length", len(setData)); # get length - it will counting 0.


